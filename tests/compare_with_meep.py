"""Legacy entry point — runs the extensive MEEP validation suite."""

import os
import sys
import unittest


def main():
    os.environ.setdefault("PYOPENCL_CTX", "0")
    suite = unittest.defaultTestLoader.loadTestsFromName("tests.test_meep_validation")
    result = unittest.TextTestRunner(verbosity=2).run(suite)
    sys.exit(0 if result.wasSuccessful() else 1)


if __name__ == "__main__":
    main()
